@pytest.mark.timeout_seconds(5)
def test_repo_type_ignore_comments_follow_policy() -> None:
    blanket_offenders: list[str] = []
    reason_offenders: list[str] = []
    blanket_marker = "# type:" + " ignore"
    coded_marker_prefix = "# type:" + " ignore["
    external_library_suffix = (
        f"# reason: external library has no type support, see {EXTERNAL_LIBRARY_POLICY_REF}"
    )
    autogenerated_suffix = (
        f"# reason: autogenerated code has no type support, see {AUTOGENERATED_POLICY_REF}"
    )

    for rel_path, line_number, line in _repo_type_ignore_lines():
        if blanket_marker in line and coded_marker_prefix not in line:
            blanket_offenders.append(f"{rel_path}:{line_number}")
        if coded_marker_prefix not in line:
            continue
        if line.endswith(external_library_suffix) or line.endswith(autogenerated_suffix):
            continue
        reason_offenders.append(f"{rel_path}:{line_number}")

    assert blanket_offenders == []

    policy_prefix = "# type:" + " ignore"
    policy_message = (
        f"Every '{policy_prefix}[...]' must end with either '# reason: external library has no "
        "type support, see docs/agents/type-ignore-policy.md#external-library' or '# reason: "
        "autogenerated code has no type support, see "
        "docs/agents/type-ignore-policy.md#autogenerated-code'."
    )
    assert reason_offenders == [], policy_message


def test_zero_test_file_suppressions() -> None:
    """Test files must have zero suppressions."""
    test_suppressions: list[str] = []
    for rel_path, line_number, _ in _repo_type_ignore_lines():
        if "tests/" not in rel_path and "tests\\" not in rel_path:
            continue
        test_suppressions.append(f"{rel_path}:{line_number}")
    assert test_suppressions == [], (
        "Test files must not contain suppressions. "
        f"Found {len(test_suppressions)} in test files: {test_suppressions}"
    )


def test_external_library_policy_document_exists() -> None:
    policy_path = WORKSPACE_ROOT / EXTERNAL_LIBRARY_POLICY_REF.split("#", maxsplit=1)[0]
    assert policy_path.is_file(), f"Missing policy document: {EXTERNAL_LIBRARY_POLICY_REF}"


def test_targeted_runtime_modules_no_longer_need_explicit_any_ignores() -> None:
    offenders = [
        f"{rel_path}:{line_number}"
        for rel_path, line_number, line in _repo_type_ignore_lines()
        if rel_path in TARGETED_RUNTIME_IGNORE_FREE_PATHS and "# type: ignore[explicit-any]" in line
    ]
    assert offenders == [], (
        "The first-party Pydantic compatibility shim should eliminate broad model-level "
        f"explicit-any suppressions in the targeted runtime modules. Found: {offenders}"
    )


@pytest.mark.timeout_seconds(5)
def test_zero_runtime_pyright_directives() -> None:
    """Runtime code must have zero runtime directives from pyright."""
    runtime_pyright_directives: list[str] = []
    for rel_path, line_number, line in _repo_type_ignore_lines():
        if "tests/" in rel_path or "tests\\" in rel_path:
            continue
        if "# py" + "right:" in line:
            runtime_pyright_directives.append(f"{rel_path}:{line_number}")
    assert runtime_pyright_directives == [], (
        "Runtime code must not contain directives from py" + "right. "
        "Use # type:" + " ignore[...] with reason comments instead. "
        f"Found {len(runtime_pyright_directives)}: {runtime_pyright_directives}"
    )


def test_config_authority_mypy_ini_only() -> None:
    """pyproject.toml must not define [tool.mypy]; mypy.ini is the sole source of truth."""
    pyproject = _load_pyproject()
    tool = pyproject.get("tool")
    assert isinstance(tool, dict), "Expected [tool] section in pyproject.toml"
    mypy_config = tool.get("mypy")
    assert mypy_config is None, (
        "Found [tool.mypy] in pyproject.toml; mypy.ini is the sole authority. "
        f"Config: {mypy_config}"
    )
