def test_mypy_requires_error_codes_on_type_ignores() -> None:
    parser = _load_mypy_config()
    enabled_codes = {
        code.strip()
        for code in parser["mypy"].get("enable_error_code", "").split(",")
        if code.strip()
    }

    assert "ignore-without-code" in enabled_codes


