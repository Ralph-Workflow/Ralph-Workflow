"""Frozen golden test pinning the 7 built-in AgentSupport entries.

THIS FILE IS A FROZEN CONTRACT.

It exists to prove the BuiltinAgentSpec refactor in
``ralph/agents/builtin.py`` is a no-op.  The 7 entries below MUST match the
``_BUILTIN_AGENT_SUPPORTS`` tuple byte-for-byte in (name, transport,
parser_factory, strategy_factory, config.cmd, session_flag, json_parser).

Do not edit this file unless you are:
  1. Adding a NEW built-in agent (append to the expected list).
  2. Changing the wire format of an existing parser or strategy class
     (update the expected parser_factory / strategy_factory reference).

The 7 declarations live in ``ralph/agents/builtin.py`` via
``_BUILTIN_AGENT_SUPPORTS``; this test enforces the same shape after the
BuiltinAgentSpec dataclass refactor.
"""

from __future__ import annotations

from ralph.agents.builtin import builtin_supports
from ralph.agents.execution_state._base import BaseExecutionStrategy
from ralph.agents.execution_state._factory import _make_agy_strategy
from ralph.agents.execution_state.claude_execution_strategy import ClaudeExecutionStrategy
from ralph.agents.execution_state.claude_interactive_execution_strategy import (
    ClaudeInteractiveExecutionStrategy,
)
from ralph.agents.execution_state.generic_execution_strategy import GenericExecutionStrategy
from ralph.agents.execution_state.opencode_execution_strategy import OpenCodeExecutionStrategy
from ralph.agents.parsers.agy import AgyParser
from ralph.agents.parsers.claude import ClaudeParser
from ralph.agents.parsers.claude_interactive import ClaudeInteractiveParser
from ralph.agents.parsers.codex import CodexParser
from ralph.agents.parsers.generic import GenericParser
from ralph.agents.parsers.opencode import OpenCodeParser
from ralph.agents.parsers.pi import PiParser
from ralph.agents.support import AgentSupport
from ralph.config.enums import AgentTransport, JsonParserType


class TestBuiltinSupportsFrozen:
    """Pins the 7 built-in AgentSupport entries that ``builtin_supports()`` returns.

    The BuiltinAgentSpec dataclass refactor must not change any of these values.
    """

    EXPECTED_BUILTIN_SUPPORTS: tuple[
        tuple[str, AgentTransport, type, object, str, str | None, JsonParserType],
        ...,
    ] = (
        (
            "claude",
            AgentTransport.CLAUDE_INTERACTIVE,
            ClaudeInteractiveParser,
            ClaudeInteractiveExecutionStrategy,
            "claude",
            "--resume {}",
            JsonParserType.CLAUDE,
        ),
        (
            "claude-headless",
            AgentTransport.CLAUDE,
            ClaudeParser,
            ClaudeExecutionStrategy,
            "claude -p",
            "--resume {}",
            JsonParserType.CLAUDE,
        ),
        (
            "codex",
            AgentTransport.CODEX,
            CodexParser,
            GenericExecutionStrategy,
            "codex exec",
            None,
            JsonParserType.CODEX,
        ),
        (
            "opencode",
            AgentTransport.OPENCODE,
            OpenCodeParser,
            OpenCodeExecutionStrategy,
            "opencode",
            "--session {}",
            JsonParserType.OPENCODE,
        ),
        (
            "nanocoder",
            AgentTransport.NANOCODER,
            GenericParser,
            GenericExecutionStrategy,
            "nanocoder",
            None,
            JsonParserType.GENERIC,
        ),
        (
            "agy",
            AgentTransport.AGY,
            AgyParser,
            _make_agy_strategy,
            "agy",
            None,
            JsonParserType.GENERIC,
        ),
        (
            "pi",
            AgentTransport.PI,
            PiParser,
            GenericExecutionStrategy,
            "pi",
            "--session {}",
            JsonParserType.PI,
        ),
    )

    def test_builtin_supports_returns_seven_entries(self) -> None:
        supports = builtin_supports()
        assert len(supports) == 7, f"Expected 7 built-in supports, got {len(supports)}"

    def test_builtin_supports_names_in_order(self) -> None:
        supports = builtin_supports()
        names = tuple(s.name for s in supports)
        assert names == (
            "claude",
            "claude-headless",
            "codex",
            "opencode",
            "nanocoder",
            "agy",
            "pi",
        )

    def test_each_builtin_entry_matches_frozen_contract(self) -> None:
        supports = builtin_supports()
        for support, expected in zip(supports, self.EXPECTED_BUILTIN_SUPPORTS, strict=True):
            exp_name = expected[0]
            exp_transport = expected[1]
            exp_parser = expected[2]
            exp_strategy = expected[3]
            exp_cmd = expected[4]
            exp_session = expected[5]
            exp_json = expected[6]

            assert isinstance(support, AgentSupport), (
                f"Built-in support {exp_name!r} is not an AgentSupport"
            )
            assert support.name == exp_name, (
                f"Name mismatch: got {support.name!r}, expected {exp_name!r}"
            )
            assert support.transport is exp_transport, (
                f"Transport mismatch for {exp_name!r}: "
                f"got {support.transport!r}, expected {exp_transport!r}"
            )
            assert support.parser_factory is exp_parser, (
                f"parser_factory mismatch for {exp_name!r}: "
                f"got {support.parser_factory!r}, expected {exp_parser!r}"
            )
            assert support.strategy_factory is exp_strategy, (
                f"strategy_factory mismatch for {exp_name!r}: "
                f"got {support.strategy_factory!r}, expected {exp_strategy!r}"
            )
            assert support.config.cmd == exp_cmd, (
                f"config.cmd mismatch for {exp_name!r}: "
                f"got {support.config.cmd!r}, expected {exp_cmd!r}"
            )
            assert support.config.session_flag == exp_session, (
                f"session_flag mismatch for {exp_name!r}: "
                f"got {support.config.session_flag!r}, expected {exp_session!r}"
            )
            assert support.config.json_parser is exp_json, (
                f"json_parser mismatch for {exp_name!r}: "
                f"got {support.config.json_parser!r}, expected {exp_json!r}"
            )

    def test_strategy_factories_are_callable_for_all_builtins(self) -> None:
        """All 7 strategy factories must be callable producing BaseExecutionStrategy subclasses.

        This guards against accidental substitution of, e.g., ``BaseExecutionStrategy``
        (the abstract default) into an interactive transport's slot.
        """
        for support in builtin_supports():
            factory = support.strategy_factory
            assert callable(factory), (
                f"strategy_factory for {support.name!r} is not callable"
            )
            instance = factory(label_scope=None, registry=None)
            assert isinstance(instance, BaseExecutionStrategy), (
                f"strategy_factory for {support.name!r} produced non-strategy: {type(instance)}"
            )

    def test_parser_factories_are_callable_for_all_builtins(self) -> None:
        """All 7 parser factories must be callable producing parser instances."""
        for support in builtin_supports():
            factory = support.parser_factory
            assert callable(factory), (
                f"parser_factory for {support.name!r} is not callable"
            )
            instance = factory()
            assert instance is not None, (
                f"parser_factory for {support.name!r} produced None"
            )
