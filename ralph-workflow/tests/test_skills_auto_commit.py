@pytest.mark.timeout_seconds(5)
def test_auto_commit_fails_closed_on_oserror(
    tmp_path: Path,
) -> None:
    """Outer guard catches OSError and returns None -- pipeline is never broken."""
    Repo.init(tmp_path)
    _track_initial_commit(tmp_path)

    def _raising_create_commit(
        _repo_root: Path, _message: str
    ) -> str:
        raise OSError("simulated filesystem failure")

    # No dirty skill-tree mutations; the helper returns None BEFORE
    # calling create_commit, so this test is a no-op smoke check. To
    # actually exercise the OSError branch we need a dirty tree plus a
    # raising create_commit.
    skill_dir = tmp_path / ".opencode" / "skills" / "brainstorming"
    skill_dir.mkdir(parents=True, exist_ok=True)
    skill_dir.joinpath("SKILL.md").write_text("# brainstorm\n", encoding="utf-8")

    result = commit_skill_updates(tmp_path, _raising_create_commit, stage_fn=MagicMock())

    assert result is None, (
        f"OSError in create_commit must be caught and return None; got: {result!r}"
    )
