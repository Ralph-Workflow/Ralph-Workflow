@pytest.mark.timeout_seconds(5)
def test_auto_commit_stages_only_skill_roots(
    tmp_path: Path, fake_create_commit: MagicMock
) -> None:
    """AC-04: stage_fn is called with paths ONLY under the FIVE skill roots.

    Pre-stages one dirty file under ``.opencode/skills/`` (allowed) and
    one dirty file outside (``src/main.go``, NOT allowed). The defensive
    filter must drop the outside path even if ``git status`` reported it.
    """
    Repo.init(tmp_path)
    _track_initial_commit(tmp_path)

    skill_dir = tmp_path / ".opencode" / "skills" / "brainstorming"
    skill_dir.mkdir(parents=True, exist_ok=True)
    skill_dir.joinpath("SKILL.md").write_text("# brainstorm\n", encoding="utf-8")

    # Dirty file OUTSIDE the skill roots -- the defensive filter must drop it
    src_dir = tmp_path / "src"
    src_dir.mkdir(parents=True, exist_ok=True)
    src_dir.joinpath("main.go").write_text("package main\n", encoding="utf-8")

    stage_spy = MagicMock()
    commit_skill_updates(tmp_path, fake_create_commit, stage_fn=stage_spy)

    stage_spy.assert_called_once()
    staged_paths = stage_spy.call_args.args[1]
    for path in staged_paths:
        assert any(path.startswith(prefix) for prefix in _SKILL_ROOT_PREFIXES), (
            f"Staged path {path!r} must be under one of the FIVE skill roots"
        )
    assert "src/main.go" not in staged_paths, (
        f"Dirty file outside skill roots MUST be filtered out; got: {staged_paths!r}"
    )
    assert ".opencode/skills/brainstorming/SKILL.md" in staged_paths, (
        f"Skill-root path MUST be staged; got: {staged_paths!r}"
    )


@pytest.mark.timeout_seconds(5)
def test_auto_commit_subject_is_deterministic_across_two_runs(
    tmp_path: Path, fake_create_commit: MagicMock
) -> None:
    """AC-01: subject is identical across consecutive runs even when bodies differ."""
    Repo.init(tmp_path)
    _track_initial_commit(tmp_path)

    # First run: stage one skill
    skill_a = tmp_path / ".opencode" / "skills" / "brainstorming"
    skill_a.mkdir(parents=True, exist_ok=True)
    skill_a.joinpath("SKILL.md").write_text("# brainstorm\n", encoding="utf-8")
    stage_spy = MagicMock()
    commit_skill_updates(tmp_path, fake_create_commit, stage_fn=stage_spy)
    first_message = fake_create_commit.call_args.args[1]

    # Second run: stage a different skill (simulate a different set of dirty paths)
    skill_b = tmp_path / ".opencode" / "skills" / "coding-standards"
    skill_b.mkdir(parents=True, exist_ok=True)
    skill_b.joinpath("SKILL.md").write_text("# coding-standards\n", encoding="utf-8")
    commit_skill_updates(tmp_path, fake_create_commit, stage_fn=MagicMock())
    second_message = fake_create_commit.call_args.args[1]

    first_subject = first_message.splitlines()[0]
    second_subject = second_message.splitlines()[0]
    assert first_subject == second_subject, (
        f"Subject must be IDENTICAL across consecutive runs; "
        f"got: {first_subject!r} vs {second_subject!r}"
    )
    assert first_subject == SKILL_AUTO_COMMIT_SUBJECT
    assert first_message != second_message, (
        "Bodies must differ when changed skill names differ"
    )


@pytest.mark.timeout_seconds(5)
def test_auto_commit_fails_closed_on_git_lock(
    tmp_path: Path, fake_create_commit: MagicMock
) -> None:
    """AC-04: a stale .git/index.lock must NOT crash the auto-commit; helper returns None.

    Pins the fail-closed contract: ``_run_git_operation_with_stale_lock_recovery``
    handles the lock transparently, and any persistent GitCommandError is
    caught by the outer guard and returns None so a broken git state
    cannot block the pipeline.
    """
    Repo.init(tmp_path)
    _track_initial_commit(tmp_path)
    skill_dir = tmp_path / ".opencode" / "skills" / "brainstorming"
    skill_dir.mkdir(parents=True, exist_ok=True)
    skill_dir.joinpath("SKILL.md").write_text("# brainstorm\n", encoding="utf-8")

    # Create a stale index.lock that points at a non-existent PID so the
    # recovery helper unlinks it. This simulates a concurrent crash that
    # left a stale lock behind.
    lock_path = tmp_path / ".git" / "index.lock"
    lock_path.write_text("", encoding="utf-8")

    stage_spy = MagicMock()
    result = commit_skill_updates(tmp_path, fake_create_commit, stage_fn=stage_spy)

    # Either the lock was recovered transparently (commit succeeded with
    # fake_create_commit called) OR the helper returned None fail-closed.
    # The contract is: never raise, never block the pipeline.
    if result is None:
        # Fail-closed branch -- the helper detected unrecoverable git state
        fake_create_commit.assert_not_called()
    else:
        # Lock was recovered -- commit succeeded
        fake_create_commit.assert_called_once()
        assert result == "f" * 40


